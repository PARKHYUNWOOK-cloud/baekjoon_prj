package baek_25314;

import java.util.Scanner;

/**
 * 25314 for - 코딩은 체육과목입니다.
 */
public class Main {
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		int n = sc.nextInt()/4;
		
		for(int i=0;i<n;i++) {
			System.out.print("long ");
		}//end for 
		System.out.print("int");
		
		
		
		
	}//main
}//class
